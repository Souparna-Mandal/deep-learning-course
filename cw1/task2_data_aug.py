def plot_curves(tag, train_loss, val_loss, train_acc, val_acc, out_file):
    fig, ax = plt.subplots(figsize=(8, 5))

    # Loss (left y-axis)
    ax.plot(epochs, train_loss, label='Train Loss', color='tab:blue')
    ax.plot(epochs, val_loss, label='Val Loss', color='tab:orange')
    ax.set_xlabel('Epoch')
    ax.set_ylabel('Loss')

    # Accuracy (right y-axis)
    ax2 = ax.twinx()
    ax2.plot(epochs, train_acc, label='Train Acc', color='tab:green', linestyle='--')
    ax2.plot(epochs, val_acc, label='Val Acc', color='tab:red', linestyle='--')
    ax2.set_ylabel('Accuracy (%)')

    # Combined legend
    lines1, labels1 = ax.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax.legend(lines1 + lines2, labels1 + labels2, loc='lower right')

    plt.title(f'{tag}: Loss and Accuracy vs Epoch')
    plt.tight_layout()
    fig.savefig(out_file, dpi=200)
    plt.close(fig)


